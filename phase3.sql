/*Create customer table*/
CREATE TABLE CUSTOMERS (
    ID INT PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL,
    ADDRESS VARCHAR(255),
    PHONE STRING,
    EMAIL VARCHAR(255),
    PASSWORD VARCHAR(255),
    BIRTHDATE DATE
    --date is a type of its own will accept YYYY-MM-DD
);

/*Create table of carts*/
CREATE TABLE CART(
  CARTID INT PRIMARY KEY,
  NAME STRING NOT NULL,
  DESCRIPTION STRING,
  CART_OWNER_ID INT
);

/*Create table of products*/
CREATE TABLE PRODUCT(
  ID INT PRIMARY KEY,
  NAME STRING,
  DESCRIPTION STRING,
  MANUFACTURER STRING,
  VISUAL IMAGE
); 
/*Create table of orders*/
CREATE TABLE ORDERS (
  ID INT PRIMARY KEY,
  READY_DATE DATE,
  DELIVERY_OR_PICKUP STRING,
  FULFILLED DATE,
  COST INT
);


